            </div>
        </div>
        
        <div class="tip-container">
            <div class="tip-header">Tip 4: Make It Social</div>
            <div class="tip-content">
                <p>The social dimension of fitness provides accountability and enjoyment:</p>
                <ul>
                    <li>Find a reliable workout partner who shares similar goals</li>
                    <li>Join group fitness classes or sports teams</li>
                    <li>Share goals with friends or on social media for accountability</li>
                    <li>Consider working with a personal trainer for structured guidance</li>
                </ul>
                <p>Having others involved in your fitness journey creates external motivation when your internal drive fluctuates.</p>
            </div>
        </div>
        
        <div class="tip-container">
            <div class="tip-header">Tip 5: Cultivate Variety and Enjoyment</div>
            <div class="tip-content">
                <p>Boredom is a motivation killer. Keep your fitness routine fresh by:</p>
                <ul>
                    <li>Trying different workout styles and modalities</li>
                    <li>Setting new types of challenges for yourself</li>
                    <li>Finding physical activities you genuinely enjoy</li>
                    <li>Changing your environment (outdoor workouts, new gym, etc.)</li>
                    <li>Creating engaging playlists or finding podcasts/audiobooks for cardio sessions</li>
                </ul>
                <p>The most sustainable workout routine is one you actually look forward to doing.</p>
