    <main>
        <div class="tip-container">
            <div class="tip-header">Tip 1: Set Specific, Measurable Goals</div>
            <div class="tip-content">
                <p>Vague intentions like "get in shape" rarely provide lasting motivation. Instead, create specific goals with clear metrics:</p>
                <ul>
                    <li><strong>Strength goals:</strong> "Increase my squat by 20 pounds in 8 weeks"</li>
                    <li><strong>Endurance goals:</strong> "Run a 5K in under 30 minutes by June"</li>
                    <li><strong>Consistency goals:</strong> "Exercise 4 times per week for the next month"</li>
                </ul>
                <p>Track your progress regularly and celebrate small victories along the way. Having concrete evidence of your improvement provides powerful motivation to continue.</p>
            </div>
        </div>
        
        <div class="tip-container">
            <div class="tip-header">Tip 2: Find Your "Why"</div>
            <div class="tip-content">
                <p>Connect your fitness journey to deeper personal values and motivations:</p>
                <ul>
                    <li><strong>Health:</strong> Reducing disease risk and increasing longevity</li>
                    <li><strong>Mental wellbeing:</strong> Managing stress, anxiety, or depression</li>
                    <li><strong>Performance:</strong> Improving in a sport or activity you enjoy</li>
                    <li><strong>Energy:</strong> Having more vitality for work and family</li>
                </ul>
                <p>When motivation wanes, reconnecting with your fundamental reasons for exercising can reignite your commitment.</p>
            </div>
        </div>
        
        <div class="tip-container">
            <div class="tip-header">Tip 3: Design for Consistency, Not Perfection</div>
            <div class="tip-content">
                <p>Sustainable fitness is about showing up consistently, not having perfect workouts:</p>
                <ul>
                    <li>Create a realistic schedule that fits your life</li>
                    <li>Develop minimum viable workout plans for busy days (even 15-20 minutes counts)</li>
                    <li>Focus on habit-building rather than intensity in the early stages</li>
                    <li>Plan ahead for known obstacles like travel or busy work periods</li>
                </ul>
                <p>Remember that consistency over time delivers results, not occasional intense efforts.</p>
                <div class="highlight">
                    Consistency over time delivers results, not occasional intense efforts.
